package model;

/**
 * Enumeratietype voor markering van een veld in Boter, Kaas 
 * en Eieren
 */
public enum Markering {
  /**
   * Markering die aangeeft dat het veld nog leeg is
   */
  LEEG, 
  
  /**
   * Markering die aangeeft dat het veld een kruis bevat
   */
  KRUIS,
  
  /**
   * Markering die aangeeft dat het veld een nul bevat
   */
  NUL
}
